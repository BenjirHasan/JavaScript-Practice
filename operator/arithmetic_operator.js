// Arithmetic operators
let x = 5+2; //addition
console.log(x);

let y = 10-4; //substraction
console.log(y);

let z = 10*4; //multiplication
console.log(z);

let a = 10/5; //division
console.log(a);

let b = 10**4; //exponential
console.log(b);

let c = 4;
d = c++;  //postfix incremental
console.log(c);
console.log(d);

let e = 4;
f = ++e;  //prefix incremental
console.log(e);
console.log(f);

let g = 4;
h = g--;  //postfix decremental
console.log(g);
console.log(h);

let i = 4;
j = --i;  //prefix decremental
console.log(i);
console.log(j);

