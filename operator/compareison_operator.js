// (==)equality operator (check only variables value)
var x = 5;
var y = "Benjir";

if (x == y) 
{
  console.log("true");
} 
else 
{
  console.log("false");
}

// (!=)inequality operator
var x = 5;
var y = "Benjir";

if (x != y) 
{
  console.log("true");
} 
else 
{
  console.log("false");
}

// (===)strict equality operator (check variables value typr as well as variables value)
var x = 5;
var y = "5";

if (x === y) 
{
  console.log("true");
} 
else 
{
  console.log("false");
}

// (!==)strict inequality operator 
var x = 5;
var y = "5";

if (x === y) 
{
  console.log("true");
} 
else 
{
  console.log("false");
}

// (>)greater than operator
var x = 5;
var y = 10;

if (x > y) 
{
  console.log("true");
} 
else 
{
  console.log("false");
}

// (<)less than operator
var x = 5;
var y = 10;

if (x < y) 
{
  console.log("true");
} 
else 
{
  console.log("false");
}

// (>=)greater than or equal operator
